func TestBuildContainerResolvconfUsesHostAndPublicFallbacks(t *testing.T) {
	host := []byte(`# Generated by NetworkManager
search lab.example
options edns0 trust-ad
nameserver 127.0.0.53
nameserver 192.168.2.1
nameserver 192.168.2.2
`)
	got := string(buildContainerResolvconf(host))
	for _, want := range []string{
		"search lab.example",
		"options edns0 trust-ad",
		"nameserver 192.168.2.1",
		"nameserver 1.1.1.1",
		"nameserver 8.8.8.8",
	} {
		if !strings.Contains(got, want) {
			t.Fatalf("managed resolv.conf missing %q:\n%s", want, got)
		}
	}
	for _, unwanted := range []string{"nameserver 127.0.0.53", "nameserver 192.168.2.2"} {
		if strings.Contains(got, unwanted) {
			t.Fatalf("managed resolv.conf contains %q:\n%s", unwanted, got)
		}
	}
}

func TestBuildContainerResolvconfFallsBackWhenHostHasNoNameserver(t *testing.T) {
	got := nameserversInConfig(buildContainerResolvconf([]byte("# Generated by NetworkManager\n")))
	want := []string{"1.1.1.1", "8.8.8.8"}
	if !reflect.DeepEqual(got, want) {
		t.Fatalf("nameservers = %#v, want %#v", got, want)
	}
}

func TestWriteResolvconfInPlaceKeepsInodeAndSkipsUnchangedWrite(t *testing.T) {
	path := filepath.Join(t.TempDir(), "run", "resolv.conf")
	first := []byte("nameserver 1.1.1.1\n")
	if err := writeResolvconfInPlace(path, first); err != nil {
		t.Fatal(err)
	}
	before, err := os.Stat(path)
	if err != nil {
		t.Fatal(err)
	}
	beforeModTime := before.ModTime()
	time.Sleep(10 * time.Millisecond)
	if err := writeResolvconfInPlace(path, first); err != nil {
		t.Fatal(err)
	}
	unchanged, err := os.Stat(path)
	if err != nil {
		t.Fatal(err)
	}
	if !unchanged.ModTime().Equal(beforeModTime) {
		t.Fatalf("unchanged file modtime = %v, want %v", unchanged.ModTime(), beforeModTime)
	}
	if err := writeResolvconfInPlace(path, []byte("nameserver 8.8.8.8\n")); err != nil {
		t.Fatal(err)
	}
	after, err := os.Stat(path)
	if err != nil {
		t.Fatal(err)
	}
	if !os.SameFile(before, after) {
		t.Fatal("managed resolv.conf inode changed during update")
	}
}


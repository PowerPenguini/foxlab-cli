func TestManagedResolvconfValidityRequiresUsableNameserver(t *testing.T) {
	path := filepath.Join(t.TempDir(), "resolv.conf")
	if err := os.WriteFile(path, []byte("nameserver 127.0.0.53\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	if managedResolvconfValid(path) {
		t.Fatal("loopback-only managed resolver considered valid")
	}
	if err := os.WriteFile(path, []byte("nameserver 1.1.1.1\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	if !managedResolvconfValid(path) {
		t.Fatal("public managed resolver considered invalid")
	}
}

func nameserversInConfig(config []byte) []string {
	var nameservers []string
	for _, line := range strings.Split(string(config), "\n") {
		fields := strings.Fields(line)
		if len(fields) > 1 && fields[0] == "nameserver" {
			nameservers = append(nameservers, fields[1])
		}
	}
	return nameservers
}

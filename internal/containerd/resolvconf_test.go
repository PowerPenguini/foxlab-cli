func TestSyncContainerResolvconfKeepsLastGoodConfigWhenHostReadFails(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)
	t.Setenv("SUDO_USER", "")
	l := &lab.Lab{ID: "demo"}
	ct := lab.Container{ID: "web"}
	hostPath := filepath.Join(t.TempDir(), "resolv.conf")
	if err := os.WriteFile(hostPath, []byte("nameserver 192.168.2.1\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	managedPath, err := syncContainerResolvconfFrom(l, ct, hostPath)
	if err != nil {
		t.Fatal(err)
	}
	want, err := os.ReadFile(managedPath)
	if err != nil {
		t.Fatal(err)
	}
	if err := os.Remove(hostPath); err != nil {
		t.Fatal(err)
	}
	gotPath, err := syncContainerResolvconfFrom(l, ct, hostPath)
	if err != nil {
		t.Fatalf("sync with missing host resolver: %v", err)
	}
	if gotPath != managedPath {
		t.Fatalf("managed path = %q, want %q", gotPath, managedPath)
	}
	got, err := os.ReadFile(managedPath)
	if err != nil {
		t.Fatal(err)
	}
	if !reflect.DeepEqual(got, want) {
		t.Fatalf("managed resolver changed after host read failure:\n%s", got)
	}
}

